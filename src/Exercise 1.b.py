# Program to compute the area of a circle

# Step 1: Ask user for radius input
radius = float(input("Enter the radius of the circle: "))

# Step 2: Calculate area using the formula
area = 3.14 * radius ** 2

# Step 3: Display the result with proper label
print("\n--- Circle Area Result ---")
print("radius:", radius)
print("Area of the circle:", area)
